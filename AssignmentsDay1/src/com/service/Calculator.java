package com.service;

public class Calculator {
	
	public int add(int a, int b) {
		return a+b;
	}

	public int sub(int a, int b) {
		return a-b;
	}

	public int mul(int a, int b) {
		return a*b;
	}

	public float div(int a, int b) {
		return a/b;
	}

	public int reminder(int a, int b) {
		return a%b;
	}
}
